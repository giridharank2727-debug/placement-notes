

let a = 100;
let b = 20;
let c;

c = a + b;     // +  → Addition operator       // Output: 120
console.log(c);
c = a - b;     // -  → Subtraction operator    // Output: 80
console.log(c);
c = a * b;     // *  → Multiplication operator // Output: 2000
console.log(c);
c = a / b;     // /  → Division operator       // Output: 5
console.log(c);
c = a % b;     // %  → Modulus operator        // Output: 0
console.log(c);
c = 2 ** 3;    // ** → Exponentiation operator // Output: 8

console.log(c);

